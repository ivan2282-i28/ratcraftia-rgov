def _ensure_user(
    session: Session,
    *,
    uin: str,
    uan: str,
    login: str,
    first_name: str,
    last_name: str,
    patronymic: str,
    password: str,
    permissions: list[str],
    org_id: int | None,
    position_title: str,
) -> User:
    user = session.exec(select(User).where(User.login == login)).first()
    permissions_value = serialize_permissions(permissions)
    if user:
        if user.permissions != permissions_value:
            user.permissions = permissions_value
            session.add(user)
            session.flush()
        return user
    user = User(
        uin=uin,
        uan=uan,
        login=login,
        first_name=first_name,
        last_name=last_name,
        patronymic=patronymic,
        password_hash=get_password_hash(password),
        permissions=permissions_value,
        org_id=org_id,
        position_title=position_title,
    )
    session.add(user)
    session.flush()
    return user

def _ensure_law(
    session: Session,
    *,
    title: str,
    slug: str,
    level: str,
    current_text: str,
    adopted_via: str,
    author_id: int | None = None,
    status: str = "active",
    version: int = 1,
) -> Law:
    law = session.exec(select(Law).where(Law.slug == slug)).first()
    if law:
        changed = False

        if law.title != title:
            law.title = title
            changed = True
        if law.level != level:
            law.level = level
            changed = True
        if law.current_text != current_text:
            law.current_text = current_text
            changed = True
        if law.adopted_via != adopted_via:
            law.adopted_via = adopted_via
            changed = True
        if law.author_id != author_id:
            law.author_id = author_id
            changed = True
        if law.status != status:
            law.status = status
            changed = True
        if law.version != version:
            law.version = version
            changed = True

        if changed:
            session.add(law)
            session.flush()
        return law

    law = Law(
        title=title,
        slug=slug,
        level=level,
        current_text=current_text,
        adopted_via=adopted_via,
        author_id=author_id,
        status=status,
        version=version,
    )
    session.add(law)
    session.flush()
    return law

